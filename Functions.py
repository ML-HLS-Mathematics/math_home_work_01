def f_name(arg):
    return f'f({arg})'


def g_name(arg):
    return f'g({arg})'


def h_name(arg):
    return f'h({arg})'


def i_name(arg):
    return f'i({arg})'


def f(x):
    """Возвращает результат функции f(x) из задания"""
    return x ** 2 - 30 * x + 1


def g(x):
    """Возвращает результат функции g(x) из задания"""
    return -x ** 3 + 5 * x - 7 * x ** 2 + 17


def h(x):
    """Возвращает результат функции h(x) из задания"""
    return x ** 2 - 1 / x


def i(x):
    """Возвращает результат функции i(x) из задания"""
    return 1 / x ** 5 + x ** 17 + 8
